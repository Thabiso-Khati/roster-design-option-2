// DeepString maps the shape of `en` to all-string values so that other
// language files can be typed against the same structure without literal
// type conflicts (e.g. French "Enregistrer" can satisfy the key that holds
// English "Save").
type DeepString<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepString<T[K]>;
};

export type TranslationKeys = DeepString<typeof en>;
